library(PetfindeR)

## ------------------------------------------------------------------------
key <- Sys.getenv('PETFINDER_KEY')

## ------------------------------------------------------------------------
pf <- Petfinder(key)

## ------------------------------------------------------------------------
cats <- pf$breed.list('cat')
